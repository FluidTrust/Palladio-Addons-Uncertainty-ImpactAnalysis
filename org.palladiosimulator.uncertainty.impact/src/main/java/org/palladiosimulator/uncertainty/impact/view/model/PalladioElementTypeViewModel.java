package org.palladiosimulator.uncertainty.impact.view.model;

public class PalladioElementTypeViewModel {

	private String name;

	private String id;

	private String type;

	public PalladioElementTypeViewModel() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
